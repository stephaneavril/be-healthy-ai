require('dotenv').config();
const express = require("express");
const cors = require("cors");
const Replicate = require("replicate");
const path = require("path");

const app = express();

app.use(cors());
app.use(express.json());

// Servir archivos estÃ¡ticos del frontend
app.use(express.static(path.join(__dirname, "frontend")));

const replicate = new Replicate({
  auth: process.env.REPLICATE_API_TOKEN
});

// Ruta POST para generar imagen
app.post("/generate", async (req, res) => {
  try {
    const { respuestas } = req.body;

    if (!respuestas || respuestas.length < 4) {
      return res.status(400).json({ error: "Se requieren 4 respuestas para generar la imagen" });
    }

    const finalPrompt = `Una ilustraciÃ³n digital inspiradora y motivacional que represente un estilo de vida equilibrado y feliz. 
    La imagen debe mostrar un ambiente armonioso con colores suaves y naturales, reflejando **paz, bienestar y alegrÃ­a**. 
    
    ðŸŽ¨ **Concepto**:
    Se debe visualizar **una persona radiante y feliz** disfrutando de su bienestar, rodeada de un entorno positivo. 
    Cada elemento debe integrarse de manera fluida en la escena para transmitir una sensaciÃ³n de plenitud.
    
    âœ¨ **Elementos Clave**:
    - **AlimentaciÃ³n:** ${respuestas[0]} - Representado por una comida balanceada y apetitosa en la escena.
    - **Actividad FÃ­sica:** ${respuestas[1]} - Expresado a travÃ©s de una postura activa y enÃ©rgica, como yoga, caminata o estiramientos.
    - **Salud Mental:** ${respuestas[2]} - Reflejado con una expresiÃ³n relajada y un fondo sereno que inspire calma.
    - **Descanso:** ${respuestas[3]} - Sugiere una atmÃ³sfera acogedora y reparadora, con luz cÃ¡lida y elementos que evocan tranquilidad.
    
    ðŸš€ **Requisitos TÃ©cnicos**:
    - **UNA SOLA IMAGEN**, sin collage ni elementos superpuestos.
    - **Colores cÃ¡lidos y naturales**, evitando tonos caÃ³ticos.
    - **EstÃ©tica profesional y realista**, sin deformaciones.
    - **SensaciÃ³n de armonÃ­a**, evitando elementos dispersos o confusos.
    
    ðŸ“Œ La imagen final debe evocar **motivaciÃ³n, bienestar y alegrÃ­a**, ideal para inspirar a las personas a mejorar su calidad de vida.`;
    
    

    console.log(`Generando imagen para: "${finalPrompt}"`);

    const prediction = await replicate.predictions.create({
      version: "ac732df83cea7fff18b8472768c88ad041fa750ff7682a21affe81863cbe77e4",
      input: { prompt: finalPrompt }
    });

    if (!prediction || !prediction.id) {
      return res.status(500).json({ error: "No se pudo iniciar la predicciÃ³n en Replicate" });
    }

    let status = prediction.status;
    let imageUrl = null;

    while (status !== "succeeded" && status !== "failed") {
      console.log(`Estado: ${status}, esperando...`);
      await new Promise(resolve => setTimeout(resolve, 3000));
      const updatedPrediction = await replicate.predictions.get(prediction.id);
      status = updatedPrediction.status;
      if (status === "succeeded" && updatedPrediction.output && updatedPrediction.output.length > 0) {
        imageUrl = updatedPrediction.output[0];
        break;
      }
    }

    if (!imageUrl) {
      return res.status(500).json({ error: "No se recibiÃ³ la imagen" });
    }

    console.log("Imagen generada:", imageUrl);
    res.json({ image_url: imageUrl });
  } catch (error) {
    console.error("Error interno:", error);
    res.status(500).json({ error: "Error interno del servidor" });
  }
});

